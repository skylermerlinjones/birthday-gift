    function heartBurst(e) {
      const btn = e.currentTarget;
      const heart = document.createElement("span");
      heart.classList.add("pop-heart");
      heart.innerText = "💖";

      const x = e.offsetX;
      const y = e.offsetY;

      heart.style.left = x + "px";
      heart.style.top = y + "px";

      btn.appendChild(heart);

      setTimeout(() => {
        heart.remove();
      }, 800);
    }
